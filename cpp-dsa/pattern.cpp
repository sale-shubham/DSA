#include <bits/stdc++.h>
using namespace std;

void simplepattern(int n)
{
    cout << "hello111111" << endl;

    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < n; j++)
        {
            cout << "*";
        }
        cout << endl;
    }
}
int main()
{
    int n = 5;

    simplepattern(n);
    return 0;
}